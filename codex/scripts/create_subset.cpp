struct TestRow {
    std::string filename;
    int classId = 0;
};

int parsePositive(const char* value, const char* name) {
    int result = 0;
    const std::string text(value);
    const auto parsed = std::from_chars(text.data(), text.data() + text.size(), result);
    if (parsed.ec != std::errc() || parsed.ptr != text.data() + text.size() || result <= 0) {
        throw std::invalid_argument(std::string(name) + " must be a positive integer.");
    }
    return result;
}

std::filesystem::path findExisting(
    const std::vector<std::filesystem::path>& candidates,
    const std::string& description) {
    for (const auto& candidate : candidates) {
        if (std::filesystem::exists(candidate)) {
            return candidate;
        }
    }
    throw std::runtime_error("Could not find " + description + ".");
}

std::vector<TestRow> readTestRows(const std::filesystem::path& csvPath) {
    std::ifstream input(csvPath);
    if (!input) {
        throw std::runtime_error("Could not open test labels: " + csvPath.string());
    }
    std::string line;
    std::getline(input, line);
    std::vector<TestRow> rows;
    while (std::getline(input, line)) {
        if (line.empty()) {
            continue;
        }
        std::vector<std::string> fields;
        std::stringstream stream(line);
        std::string field;
        while (std::getline(stream, field, ';')) {
            fields.push_back(field);
        }
        if (fields.size() < 8) {
            throw std::runtime_error("Invalid test label row: " + line);
        }
        rows.push_back({fields[0], std::stoi(fields[7])});
    }
    return rows;
}

std::string classDirectoryName(const int classId) {
    std::string name = std::to_string(classId);
    return std::string(5U - std::min<std::size_t>(5U, name.size()), '0') + name;
}

std::vector<std::filesystem::path> listImages(
    const std::filesystem::path& directory) {
    std::vector<std::filesystem::path> images;
    for (const auto& entry : std::filesystem::directory_iterator(directory)) {
        if (entry.is_regular_file() && entry.path().extension() == ".ppm") {
            images.push_back(entry.path());
        }
    }
    std::sort(images.begin(), images.end());
    return images;
}

void prepareOutput(const std::filesystem::path& outputRoot) {
    if (std::filesystem::exists(outputRoot)) {
        const auto marker = outputRoot / ".gtsrb_subset";
        if (!std::filesystem::exists(marker)) {
            throw std::runtime_error(
                "Output directory already exists without .gtsrb_subset marker: "
                + outputRoot.string());
        }
        std::filesystem::remove_all(outputRoot);
    }
    std::filesystem::create_directories(outputRoot);
    std::ofstream(outputRoot / ".gtsrb_subset")
        << "Generated by cppcnn_create_subset. Safe for this tool to replace.\n";
}

void copyFile(
    const std::filesystem::path& source,
    const std::filesystem::path& destination) {
    std::filesystem::create_directories(destination.parent_path());
    std::filesystem::copy_file(
        source,
        destination,
        std::filesystem::copy_options::overwrite_existing);
}

