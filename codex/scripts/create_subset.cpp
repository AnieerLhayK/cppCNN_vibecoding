int main(int argc, char* argv[]) {
    try {
        const std::filesystem::path sourceRoot =
            argc > 1 ? std::filesystem::path(argv[1]) : "datasets/GTSRB";
        const std::filesystem::path outputRoot =
            argc > 2 ? std::filesystem::path(argv[2]) : "datasets/GTSRB_subset";
        const int classCount = argc > 3 ? parsePositive(argv[3], "class_count") : 10;
        const int trainingImagesPerClass =
            argc > 4 ? parsePositive(argv[4], "images_per_class") : 500;
        if (trainingImagesPerClass < 500 || trainingImagesPerClass > 1000) {
            throw std::invalid_argument("images_per_class must be between 500 and 1000.");
        }

        const auto trainingRoot = findExisting({
            sourceRoot / "GTSRB" / "Final_Training" / "Images",
            sourceRoot / "Final_Training" / "Images",
        }, "GTSRB Final_Training/Images");
        const auto testRoot = findExisting({
            sourceRoot / "GTSRB" / "Final_Test" / "Images",
            sourceRoot / "Final_Test" / "Images",
        }, "GTSRB Final_Test/Images");
        const auto testCsv = findExisting({
            sourceRoot / "GT-final_test.csv",
            sourceRoot / "GTSRB" / "GT-final_test.csv",
        }, "GTSRB GT-final_test.csv");

        std::vector<std::pair<int, std::vector<std::filesystem::path>>> eligibleClasses;
        for (const auto& entry : std::filesystem::directory_iterator(trainingRoot)) {
            if (!entry.is_directory()) {
                continue;
            }
            const int classId = std::stoi(entry.path().filename().string());
            auto images = listImages(entry.path());
            if (images.size() >= static_cast<std::size_t>(trainingImagesPerClass)) {
                eligibleClasses.emplace_back(classId, std::move(images));
            }
        }
        std::sort(eligibleClasses.begin(), eligibleClasses.end(), [](const auto& left, const auto& right) {
            return left.first < right.first;
        });
        if (eligibleClasses.size() < static_cast<std::size_t>(classCount)) {
            throw std::runtime_error(
                "Not enough classes contain the requested number of training images.");
        }
        eligibleClasses.resize(static_cast<std::size_t>(classCount));

        prepareOutput(outputRoot);
        std::set<int> selectedClassIds;
        std::size_t copiedTraining = 0;
        for (const auto& [classId, images] : eligibleClasses) {
            selectedClassIds.insert(classId);
            const auto destination = outputRoot / "train" / classDirectoryName(classId);
            for (int index = 0; index < trainingImagesPerClass; ++index) {
                copyFile(images[static_cast<std::size_t>(index)], destination / images[index].filename());
                ++copiedTraining;
            }
        }

        const auto testRows = readTestRows(testCsv);
        std::size_t copiedTest = 0;
        for (const auto& row : testRows) {
            if (selectedClassIds.count(row.classId) == 0) {
                continue;
            }
            copyFile(
                testRoot / row.filename,
                outputRoot / "test" / classDirectoryName(row.classId) / row.filename);
            ++copiedTest;
        }

        std::ofstream manifest(outputRoot / "subset_manifest.txt");
        manifest
            << "source=" << std::filesystem::absolute(sourceRoot).string() << '\n'
            << "classes=" << classCount << '\n'
            << "training_images_per_class=" << trainingImagesPerClass << '\n'
            << "training_images=" << copiedTraining << '\n'
            << "test_images=" << copiedTest << '\n'
            << "class_ids=";
        bool first = true;
        for (const int classId : selectedClassIds) {
            manifest << (first ? "" : ",") << classId;
            first = false;
        }
        manifest << '\n';

        std::ofstream labels(outputRoot / "labels.txt");
        for (const int classId : selectedClassIds) {
            labels << classNames.at(static_cast<std::size_t>(classId)) << '\n';
        }

        std::cout
            << "Created GTSRB subset at: " << std::filesystem::absolute(outputRoot).string() << '\n'
            << "Classes: " << classCount << '\n'
            << "Training images: " << copiedTraining << '\n'
            << "Test images: " << copiedTest << '\n';
        return 0;
    } catch (const std::exception& error) {
        std::cerr << "Error: " << error.what() << '\n';
        return 1;
    }
}
